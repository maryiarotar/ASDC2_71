/**
 * The Measure enum represents different size for a product.
 * It includes options for XS, S, M, L, and XL which represent.
 */
public enum Measure {

    /**
    * Extra Small measure.
    */
    XS,

    /**
    * Small measure.
     */
    S,

    /**
    * Medium measure.
     */
    M,

    /**
     * Large measure.
     */
    L,

    /**
     * Extra Large measure.
     */
    XL
}

